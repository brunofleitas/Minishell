#include "ft_printf.h"

/* ************************************************************************** */
/*                                 STORE WIDTH                                */
/* ************************************************************************** */
int	store_width(t_c_data *data)
{
	int	digit;

	digit = 0;
	if (ft_isdigit(*(data->s)))
	{
		while (ft_isdigit(*(data->s)))
		{
			digit = digit * 10 + (*(data->s) - '0');
			(data->s)++;
		}
		data->str_format.width = digit;
		return (1);
	}
	else
		return (0);
}
